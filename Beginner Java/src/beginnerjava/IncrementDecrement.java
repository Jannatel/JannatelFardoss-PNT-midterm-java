package beginnerjava;

public class IncrementDecrement {

	public static void main(String[] args) {
	
		int x= 25;
		int y;
		
		
		y= --x; // 24
		System.out.println("Y = "+ y);
		
		y = x--;// 24
		System.out.println("Y ="+ y);
		
		y= ++x;//24
		System.out.println("Y= "+ y);
		
		y = x++;//24
		System.out.println("Y ="+ y);
		
		
		
		

	}

}
